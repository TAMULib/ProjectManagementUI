angular.module('mock.remoteProjectService', []).service('RemoteProjectsService', function ($q) {

    this.getRemoteProjects = function () {
        return mockRemoteProjects;
    };

    this.getByScopeId = function (remoteProjectManagerId, scopeId) {
        return $q(function (resolve, reject) {
            for (var i in mockRemoteProjects[remoteProjectManagerId]) {
                if (mockRemoteProjects[remoteProjectManagerId][i].id === scopeId) {
                    resolve(mockRemoteProjects[remoteProjectManagerId][i]);
                }
            }
            reject(undefined);
        });
    };

    this.ready = $q.defer().promise;

});